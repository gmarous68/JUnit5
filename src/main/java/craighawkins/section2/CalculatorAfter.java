package craighawkins.section2;

public class CalculatorAfter {
    //sums x and y, returns result to the caller
    public static int add(int x, int y) {
        return x + y;
    }

    //subtracts y from x, returns result to the caller
    public static int subtract(int x, int y) {
        return x - y;
    }

    //squares the input value, returns result to the caller
    public static int squareIt(int x) {
        return x * x;
    }
}
